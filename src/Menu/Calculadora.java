    private void txtD1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtD1KeyTyped
        // TODO add your handling code here:
        if (!Character.isDigit(evt.getKeyChar()) && evt.getKeyChar()!='.' && evt.getKeyChar()!='-'){
            evt.consume();
        }
        if (evt.getKeyChar()=='.' && txtD1.getText().contains(".")){
            evt.consume();
        }
        
        if (evt.getKeyChar()=='-' && txtD1.getText().contains("-")){
            evt.consume();
        } 
    }//GEN-LAST:event_txtD1KeyTyped

