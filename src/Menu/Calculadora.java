    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        if (txtD1.getText().isEmpty() || txtD2.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Favor de llenar todos los campos");
        }else{
            double num1 = Double.parseDouble(txtD1.getText());
            double num2 = Double.parseDouble(txtD2.getText());
            double respuesta = 0;
            String valor,medida;
            valor  = "";
            medida = "";

            String op = combo.getSelectedItem().toString();

            if (op.equalsIgnoreCase("voltaje (V)")){
                respuesta = num1 * num2;
                valor  = "Su voltaje es de ";
                medida = "V";
            }

            if (op.equalsIgnoreCase("corriente (A)")){
                respuesta = num1 / num2;
                valor  = "Su corriente es de ";
                medida = "A";
            }

            if (op.equalsIgnoreCase("resistencia (Ω)")){
                respuesta = num1 / num2;
                valor  = "Su resistencia es de ";
                medida = "Ω";
            }

            txtRespuesta.setText(valor + (double)Math.round(respuesta*100d)/100 + " " + medida);
            txtD1.setText("");
            txtD2.setText("");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

    private void comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboActionPerformed
        // TODO add your handling code here:
        String op = combo.getSelectedItem().toString();

        if (op.equalsIgnoreCase("voltaje (V)")){
            txtCorriente.setText("Corriente");
            txtResistencia.setText("Resistencia");
            txtRespuesta.setText("");
        }

        if (op.equalsIgnoreCase("corriente (A)")){
            txtCorriente.setText("Voltaje");
            txtResistencia.setText("Resistencia");
            txtRespuesta.setText("");           
        }

        if (op.equalsIgnoreCase("resistencia (Ω)")){
            txtCorriente.setText("Voltaje");
            txtResistencia.setText("Corriente");
            txtRespuesta.setText("");
        }
    }//GEN-LAST:event_comboActionPerformed

